package com.mycompany.MODELO;


public class ingresoProducto {
    
    private int idIngresoProdcto;
    private String fecha;
    private int idProducto;
    private double cantidad;
    private double idProveedor;
    private String descripcion;
    private int idUsuario;
    private int idMarca;

    public int getIdIngresoProdcto() {
        return idIngresoProdcto;
    }

    public void setIdIngresoProdcto(int idIngresoProdcto) {
        this.idIngresoProdcto = idIngresoProdcto;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public int getIdProducto() {
        return idProducto;
    }

    public void setIdProducto(int idProducto) {
        this.idProducto = idProducto;
    }

    public double getCantidad() {
        return cantidad;
    }

    public void setCantidad(double cantidad) {
        this.cantidad = cantidad;
    }

    public double getIdProveedor() {
        return idProveedor;
    }

    public void setIdProveedor(double idProveedor) {
        this.idProveedor = idProveedor;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public int getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(int idUsuario) {
        this.idUsuario = idUsuario;
    }

    public int getIdMarca() {
        return idMarca;
    }

    public void setIdMarca(int idMarca) {
        this.idMarca = idMarca;
    }
    
    
    
    
}
